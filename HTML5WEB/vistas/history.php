        	<h2 class="intro" >API History</h2>
            <p>
            	Lo que en HTML5 normalmente llamamos API History es en realidad solo una mejora de una vieja API que nunca tuvo una implementación oficial pero fue soportada por navegadores durante años.
                Esta vieja API estaba compuesta solo por un pequeño grupo de métodos y propiedades, algunos de ellos parte del objeto History.
                La nueva API History es precisamente una mejora de este objeto y fue incluida oficialmente en la especificación HTML como la interface History. Esta interface combina todos los viejos métodos y propiedades con algunos nuevos para trabajar y modificar el historial del navegador de acuerdo a nuestras necesidades.
